        let currentLang = 'fr';
        
        function changeLanguage(lang) {
            currentLang = lang;
            document.getElementById('langText').innerText = lang === 'fr' ? 'Français' : 'العربية';
            
            document.querySelectorAll('[data-lang-key]').forEach(el => {
                const key = el.getAttribute('data-lang-key');
                if (translations[lang] && translations[lang][key]) {
                    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                        el.placeholder = translations[lang][key];
                    } else {
                        el.innerHTML = translations[lang][key];
                    }
                }
            });
            
            if (lang === 'ar') {
                document.body.style.direction = 'rtl';
                document.body.style.textAlign = 'right';
                document.querySelectorAll('.ms-auto').forEach(el => {
                    el.classList.remove('ms-auto');
                    el.classList.add('me-auto');
                });
            } else {
                document.body.style.direction = 'ltr';
                document.body.style.textAlign = 'left';
                document.querySelectorAll('.me-auto').forEach(el => {
                    el.classList.remove('me-auto');
                    el.classList.add('ms-auto');
                });
            }
            
            localStorage.setItem('healthsys_lang', lang);
        }

        // Load saved language
        const savedLang = localStorage.getItem('healthsys_lang');
        if (savedLang && translations[savedLang]) {
            changeLanguage(savedLang);
        }

        // Initialize libraries after DOM is ready
        document.addEventListener('DOMContentLoaded', function() {
            // Initialize AOS
            if (typeof AOS !== 'undefined') {
                AOS.init({
                    duration: 800,
                    easing: 'ease-in-out',
                    once: true,
                    mirror: false,
                    offset: 100
                });
            }
            
            // Initialize PureCounter
            if (typeof PureCounter !== 'undefined') {
                try {
                    new PureCounter();
                } catch (e) {
                    console.warn('PureCounter init warning:', e);
                }
            }
            
            // Refresh AOS on resize
            window.addEventListener('resize', () => {
                if (typeof AOS !== 'undefined') AOS.refresh();
            });
        });